

public class Books
{
    String bookTitle;
    String author;
    int numberOfPages;
    String publisherName;
    
    void setBookTitle(String newBookTitle)
    {
        bookTitle = newBookTitle;
    }
    
    void setAuthor(String newAuthor)
    {
        author = newAuthor;
    }
    
    void setNumberOfPages(int newNumberOfPages)
    {
        numberOfPages = newNumberOfPages;
    }
    void setPublisherName(String newPublisherName)
    {
        publisherName = newPublisherName;
    }
        
}

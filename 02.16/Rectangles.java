
public class Rectangles
{
   int lenght;
   int width;
   
   void displaySize()
{
    System.out.print(lenght + "x" + width);
}
   
   void displayPerimeter() 
{
    System.out.print("\nPerimeter: " + lenght*2 + width*2);
}
    void displayArea()
    {
        System.out.print("\nArea: " + lenght * width);
    }
}
